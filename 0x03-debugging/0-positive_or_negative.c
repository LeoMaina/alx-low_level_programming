#include <stdlib.h>
#include <time.h>
#include <stdio.h>

/**
 * main - Entrance
 *
 * Return: Always 0 (success)
 */
int main(void)
{
	int i;

	if (i > 0)
	{
		printf("%d is positive\n", i);
	}
	else if (i < 0)
	{
		printf("%d is negative\n", i);
	}
	else if (i == 0)
	{
		printf("%d is zero\n", i);
	}

	return (0);
}

